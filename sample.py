print("Hello,\nWorld!")  # \n is used for new line
print("Hello,\tWorld!")  # \t is used for tab space
print("Hello, I am \"Bhoomi\"")  # \\ is used for double quotes
print("hello world",2,3,sep="***")  # sep is used to separate values with ***